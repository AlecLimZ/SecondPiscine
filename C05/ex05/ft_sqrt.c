#include "co5.h"

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 1)
		return (1);
	while (i * i != nb && i * i <= nb)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}
