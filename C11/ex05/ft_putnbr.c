#include "do.h"

void	ft_putnbr(int nb)
{
	long	n;
	char	p[12];
	int		i;

	i = 0;
	n = nb;
	if (n < 0)
		n *= -1;
	while (n)
	{
		p[i++] = '0' + (n % 10);
		n /= 10;
	}
	p[i] = '\0';
	if (nb < 0)
		write(1, "-", 1);
	while (--i >= 0)
		write(1, &p[i], 1);
	write(1, "\n", 1);
}
